<template>
  <div class="row">
    <div class="col-md-4 order-md-2 mb-4">
      <h4 class="d-flex justify-content-between align-items-center mb-3">
        <span class="text-muted">Your Resources</span>
        <span class="badge badge-secondary badge-pill">{{resourcesLength}}</span>
      </h4>
      <resource-search />
      <resource-list
        :resources="resources"
        :activeId="activeResource?._id"
        @on-item-click="selectResource"
      />
    </div>
    <div class="col-md-8 order-md-1">
      <h4 class="mb-3">Resource {{activeResource?._id}}
        <template v-if="hasResources">
          <button
            @click="toggleView"
            :class="`btn btn-sm ${toggleBtnClass} mr-2`">
            {{isDetailView ? 'Update' : 'Detail'}}</button>
          <resource-delete
            @on-resource-delete="hydrateResources($event, 'delete'); !hasResources ? isDetailView = true : null "
            :activeId="activeResource?._id" />
        </template>
      </h4>
      <resource-detail
        v-if="isDetailView"
        :resource="activeResource">
        <template #buttonLink>
          <router-link
            class="btn btn-outline-success"
            :to="{name: 'resourceDetailPage', params: {id: activeResource?._id}}">
            See detail page
          </router-link>
        </template>
      </resource-detail>
      <resource-update
        v-else
        @on-resource-update="hydrateResources($event, 'update')"
        :resource="activeResource" />
    </div>
  </div>
</template>

<script>
  import ResourceSearch from '@/components/ResourceSearch'
  import ResourceList from '@/components/ResourceList'
  import ResourceUpdate from '@/components/ResourceUpdate'
  import ResourceDetail from '@/components/ResourceDetail'
  import ResourceDelete from '@/components/ResourceDelete'
  import { fetchResources } from '@/actions'
  export default {
    components: {
      ResourceSearch,
      ResourceList,
      ResourceUpdate,
      ResourceDetail,
      ResourceDelete
    },
    data() {
      return {
        isDetailView: true,
        selectedResource: null,
        resources: []
      }
    },
    async created() {
      const resources = await fetchResources()
      this.resources = resources
    },
    computed: {
      resourcesLength() {
        return this.resources.length
      },
      toggleBtnClass() {
        return this.isDetailView ? 'btn-warning' : 'btn-primary'
      },
      hasResources() {
        return this.resourcesLength > 0
      },
      activeResource() {
        return this.selectedResource || (this.hasResources && this.resources[0]) || null
      }
    },
    methods: {
      toggleView() {
        this.isDetailView = !this.isDetailView
      },
      selectResource(selectedResource) {
        // TODO: it's copied by reference!!!!
        this.selectedResource = selectedResource
      },
      hydrateResources(newResource, operation) {
        const index = this.resources.findIndex(r => r._id === newResource._id)

        if (operation === 'update') {
          this.resources[index] = newResource
          this.selectResource(newResource)
        } else {
          this.resources.splice(index, 1)
          this.selectResource(this.resources[0] || null)
        }
      }
    }
  }
</script>
